export function getDateFnsLocale(locale: AppLocale) {
  return locale === "zh-CN" ? zhCN : enUS;
}

export function resolveInitialLocale(): AppLocale {
  if (typeof window === "undefined") {
    return DEFAULT_APP_LOCALE;
  }

  const stored = window.localStorage.getItem(APP_LOCALE_STORAGE_KEY);
  if (stored === "en" || stored === "zh-CN") {
    return stored;
  }

  return window.navigator.language.toLowerCase().startsWith("zh")
    ? "zh-CN"
    : DEFAULT_APP_LOCALE;
}

export function translate(
  locale: AppLocale,
  key: string,
  values?: Record<string, string | number>,
) {
  const dictionary = messages[locale];
  const template = dictionary[key] ?? messages.en[key] ?? key;

  if (!values) {
    return template;
  }

  return Object.entries(values).reduce((result, [name, value]) => {
    return result.replaceAll(`{{${name}}}`, String(value));
  }, template);
}
